#include "push_swap.h"

int	ft_atoi(const char *s)
{
	int	i;
	int	sign;
	int	nb;

	sign = 0;
	i = 0;
	nb = 0;
	while ((s[i] && (s[i] >= '\t' && s[i] <= '\r')) || (s[i] == ' '))
		i++;
	if ((s[i] == '-' ) || (s[i] == '+'))
	{
		if (s[i] == '-')
			sign++;
		i++;
	}
	while (s[i] && s[i] >= '0' && s[i] <= '9')
	{
		nb = nb * 10 + (s[i] - 48);
		i++;
	}
	if (sign == 1)
		nb = nb * -1;
	return (nb);
}
